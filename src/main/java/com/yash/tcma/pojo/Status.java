package com.yash.tcma.pojo;

/**
 * this will hold the user status like Active or nonActive
 * @author sharma.pankaj
 *
 */
public class Status {
	/**
	 * id of Status
	 */
	private int id;
	/**
	 * status name like Active or non active
	 */
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
